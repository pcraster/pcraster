        DO 115 K=1,NLAY
          DO 110 I=1,NROW
            DO 105 J=1,NCOL
              STRT(J,I,K)=HNEW(J,I,K)
  105       CONTINUE
  110     CONTINUE
  115   CONTINUE
        RESETDD=.FALSE.
      ENDIF
  
C4------PRINT TOTAL BUDGET IF REQUESTED
      IF(IBUDFL.EQ.0) GO TO 120
      CALL SGWF1BAS6V(MSUM,VBNM,VBVL,KSTP,KPER,IOUT)
      IPFLG=1
