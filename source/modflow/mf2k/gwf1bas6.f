  100 CONTINUE
C
C     IF "DDREFERENCE" OPTION IS SET FOR THIS TIME STEP, COPY HNEW INTO
C     STRT
  103 FORMAT(/,1X,'DDREFERENCE option selected:  Resetting STRT',
